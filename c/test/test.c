#include <stdio.h>
#include <stdint.h>
#include <stdlib.h>
#include <string.h>
#include <sys/random.h>

#include "../include/rijndael.h"

#define GB_TO_BYTES(n) ((n) << 30)

static const uint8_t KEY_1[16] = {
	0x88, 0x84, 0x7A, 0xFE, 0x88, 0xD3, 0xE4, 0x2C, 0x98, 0x15, 0x68, 0x3D, 0x19, 0x88, 0x3B, 0xF2
};
static const uint8_t IV_1[16] = {
	0x8C, 0x55, 0xEB, 0x76, 0x63, 0x34, 0xC6, 0x0C, 0xA7, 0xE9, 0x7E, 0x68, 0x50, 0xE3, 0x4A, 0xFA
};
static const uint8_t DATA_1[32] = {
	0xF5, 0xD3, 0xAA, 0xD9, 0x72, 0xF8, 0x86, 0x7F, 0x56, 0xE3, 0x44, 0x75, 0xDD, 0x8A, 0x2A, 0x9D,
	0x33, 0xCA, 0xE1, 0x5B, 0xCD, 0x97, 0x21, 0xF0, 0x5B, 0xB8, 0x13, 0x6E, 0xCE, 0x05, 0xEB, 0xE5
};
static const uint8_t DATA_1_ECB_ENC[sizeof(DATA_1)] = {
	0xE5, 0xDB, 0x9D, 0xAD, 0x18, 0xDC, 0xA6, 0x36, 0x24, 0x89, 0x8B, 0x56, 0x97, 0xC8, 0x1C, 0x4A,
	0x6B, 0x3E, 0xFD, 0x27, 0x63, 0x9A, 0xFD, 0xE0, 0x94, 0xD5, 0xAB, 0x5E, 0x1E, 0x8A, 0x84, 0xE3
};
static const uint8_t DATA_1_ECB_DEC[sizeof(DATA_1)] = {
	0x37, 0x27, 0x74, 0x8C, 0x51, 0x6B, 0xE7, 0x07, 0x30, 0xBE, 0xEF, 0xC8, 0x1D, 0x2A, 0x6D, 0xA1,
	0xBE, 0x80, 0xDF, 0xB0, 0xFB, 0x86, 0xD0, 0x22, 0x77, 0x97, 0x67, 0xA7, 0xAD, 0x55, 0x05, 0x65
};
static const uint8_t DATA_1_CBC_ENC[sizeof(DATA_1)] = {
	0x7F, 0x9F, 0xA6, 0x23, 0xBD, 0x16, 0x92, 0xBD, 0x81, 0x86, 0x57, 0x86, 0x9B, 0x2D, 0x01, 0x54,
	0x44, 0xB4, 0xD2, 0x88, 0x52, 0xEA, 0xD9, 0xD6, 0x92, 0x90, 0x64, 0x24, 0xB8, 0xD6, 0x43, 0xBC
};
static const uint8_t DATA_1_CBC_DEC[sizeof(DATA_1)] = {
	0xBB, 0x72, 0x9F, 0xFA, 0x32, 0x5F, 0x21, 0x0B, 0x97, 0x57, 0x91, 0xA0, 0x4D, 0xC9, 0x27, 0x5B,
	0x4B, 0x53, 0x75, 0x69, 0x89, 0x7E, 0x56, 0x5D, 0x21, 0x74, 0x23, 0xD2, 0x70, 0xDF, 0x2F, 0xF8
};

int
main(void)
{
	Rijndael_Ctx r = {0};
	Rijndael_ECB_Ctx ecb = {0};
	Rijndael_CBC_Ctx cbc = {0};
	uint8_t d[sizeof(DATA_1)] = {0};
	int ret;

	ret = rijndael_init(&r, KEY_1, sizeof(KEY_1), sizeof(IV_1));
	if (ret < 0) {
		printf("rijndael_init fail: %d\n", ret);
		return 1;
	}
	rijndael_set_mode_ECB(&ecb, &r);
	ret = rijndael_set_mode_CBC(&cbc, &r, IV_1, sizeof(IV_1));
	if (ret < 0) {
		printf("rijndael_set_mode_CBC fail: %d\n", ret);
		return 1;
	}

	ret = rijndael_encrypt_ECB(&ecb, DATA_1, sizeof(DATA_1), d, sizeof(d));
	if (ret < 0) {
		printf("rijndael_encrypt_ECB fail: %d\n", ret);
		return 1;
	}
	if (memcmp(DATA_1_ECB_ENC, d, sizeof(d)) != 0) {
		printf("DATA_1_ECB_ENC mismatch\n");
		return 1;
	}
	ret = rijndael_decrypt_ECB(&ecb, DATA_1, sizeof(DATA_1), d, sizeof(d));
	if (memcmp(DATA_1_ECB_DEC, d, sizeof(d)) != 0) {
		printf("DATA_1_ECB_DEC mismatch\n");
		return 1;
	}

	ret = rijndael_encrypt_CBC(&cbc, DATA_1, sizeof(DATA_1), d, sizeof(d));
	if (ret < 0) {
		printf("rijndael_encrypt_CBC fail: %d\n", ret);
		return 1;
	}
	if (memcmp(DATA_1_CBC_ENC, d, sizeof(d)) != 0) {
		printf("DATA_1_CBC_ENC mismatch\n");
		return 1;
	}
	ret = rijndael_decrypt_CBC(&cbc, DATA_1, sizeof(DATA_1), d, sizeof(d));
	if (memcmp(DATA_1_CBC_DEC, d, sizeof(d)) != 0) {
		printf("DATA_1_CBC_DEC mismatch\n");
		return 1;
	}

	uint8_t *d_ = malloc(GB_TO_BYTES(1));
	if (getrandom(d_, sizeof(d_), 0) != sizeof(d_)) {
		printf("getrandom alloc fail\n");
		free(d_);
		return 1;
	}
	ret = rijndael_encrypt_CBC(&cbc, d_, GB_TO_BYTES(1), d_, GB_TO_BYTES(1));
	if (ret < 0) {
		printf("rijndael_encrypt_CBC fail on d_: %d\n", ret);
		free(d_);
		return 1;
	}
	free(d_);

	return 0;
}
